			<div class="content">אז שלום לכולם !<br>
לאחרונה נחשפתי למשהו שכנראה רוב שחקני המשחק שמעו , שמועה שהמשחק ישוב חזרה.<br>
אז תתנו לי לקצר לכם ולהסביר לכם על המצב הנוכחי -<br>
1. המשחק כרגע "מתוכנת" ע"י קבוצת ילדים ללא ניסיון (לפי שידוע לי) והפרויקט רחב ממדים ומרב הסיכויים שאינו יצליח עקב מספר חודשים רב שדברים אינם זזים.<br>
2. במידה והפרויקט יקום המשחק אינו יהיה כלל דומה עקב זכויות יוצרים, התוכנה שעליה המשחק נוצר היא - "פלאש" אך למרבה הצער התוכנה במצב גסיסה והשימוש בה יופסק עוד כשנה.<br>
3. לפנות למתכנתים בפרטי זה הטרדה לכל דבר, הם אינם קשורים לפרויקט יותר ואין ביכולתם לעשות דבר.<br>
4. " יש דרך כלשהי להחזיר את אקולוקו ?" ובכן כעיקרון כן, יש למספר מצומצם של אנשים את הקבצים אך הדבר כמובן אינו חוקי ומפר זכויות יוצרים.<br>
"אז מה אפשר לעשות ? " לשלם על הזכויות יוצרים (סכום עתק) ולהקים מחדש (מה שלא יקרה).<br>
לא ידוע לי דבר על החברה אך המשיך לחפור ולמצוא עליה דברים.<br>
בצער רב,</div>

			
									
